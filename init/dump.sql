-- =========================================
-- TABLA: EJECUTIVOS
-- =========================================
CREATE TABLE CRM_USER.Ejecutivos
(
    IdEjecutivo Number GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre      Varchar(50) NOT NULL,
    Apellido    Varchar(50) NOT NULL
);

-- =========================================
-- TABLA: CLIENTES
-- =========================================
CREATE TABLE CRM_USER.Clientes
(
    IdCliente Number GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre    Varchar(50) NOT NULL,
    Apellido  Varchar(50) NOT NULL,
    Direccion Varchar(100),
    Telefono  Varchar(20)
);

-- =========================================
-- TABLA: VISITAS
-- =========================================
CREATE TABLE CRM_USER.Visitas
(
    IdVisita    Number GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdCliente   Number NOT NULL,
    IdEjecutivo Number NOT NULL,
    FechaVisita Date   NOT NULL,
    Resultado   Varchar(100),
    CONSTRAINT fk_visita_cliente FOREIGN KEY (IdCliente) REFERENCES Clientes (IdCliente),
    CONSTRAINT fk_visita_ejecutivo FOREIGN KEY (IdEjecutivo) REFERENCES Ejecutivos (IdEjecutivo)
