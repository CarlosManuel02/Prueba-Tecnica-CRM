-- =========================================
-- TABLA: VENTAS
-- =========================================
CREATE TABLE CRM_USER.Ventas
(
    IdVenta    Number GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdCliente  Number        NOT NULL,
    FechaVenta Date          NOT NULL,
    Monto      Number(10, 2) NOT NULL,
    Producto   Varchar(100),
    CONSTRAINT fk_venta_cliente FOREIGN KEY (IdCliente) REFERENCES Clientes (IdCliente)
);

-- =========================================
-- TABLA: VENTAS_ERRORES
-- =========================================
CREATE TABLE CRM_USER.Ventas_Errores
(
    IdVenta       Int,
